package io.suricate.monitoring.model.dto.widget;

import io.suricate.monitoring.model.enums.WidgetVariableType;
import io.suricate.monitoring.model.dto.AbstractDto;

import java.util.Map;

public class WidgetVariableResponse extends AbstractDto {

    private String name;

    private String description;

    private String data;

    private WidgetVariableType type;

    private boolean required;

    private Map<String, String> values;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public WidgetVariableType getType() {
        return type;
    }

    public void setType(WidgetVariableType type) {
        this.type = type;
    }

    public boolean isRequired() {
        return required;
    }

    public void setRequired(boolean required) {
        this.required = required;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public Map<String, String> getValues() {
        return values;
    }

    public void setValues(Map<String, String> values) {
        this.values = values;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o){
            return true;
        }
        if (o == null || getClass() != o.getClass()){
            return false;
        }

        WidgetVariableResponse that = (WidgetVariableResponse) o;

        return name != null ? name.equals(that.name) : that.name == null;
    }

    @Override
    public int hashCode() {
        return name != null ? name.hashCode() : 0;
    }
}
